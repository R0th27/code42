#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac != 2)
		return (write(1, "\n", 1), 1);
	i = 0;
	if (!av[1][i])
		return (write(1, "\n", 1), 0);
	while ((av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13)) && av[1][i])
		i++;
	while (av[1][i] != ' ' && av[1][i])
		write(1, &av[1][i++], 1);
	write(1, "\n", 1);
	return (0);
}
