#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	c;

	if (ac == 2)
	{
		i = -1;
		while (av[1][++i])
		{
			if ((av[1][i] >= 65 && av[1][i] < 78) || (av[1][i] >= 97 && av[1][i] < 110))
				c = av[1][i] + 13;
			else if ((av[1][i] >= 78 && av[1][i] < 91) || (av[1][i] >= 110 && av[1][i] < 123))
				c = av[1][i] - 13;
			else
				c = av[1][i];
			write (1, &c, 1);
		}
	}
	write(1, "\n", 1);
	return (0);
}
