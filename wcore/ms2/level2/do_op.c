#include <stdio.h>
#include <stdlib.h>

int	main(int ac, char **av)
{
	int	x;
	int	y;

	if (ac != 4)
		return (0);
	if (av[2][0] != '+' && av[2][0] != '-' && av[2][0] != '*' && av[2][0] != '/' && av[2][0] != '%')
		return (0);
	x = atoi(av[1]);
	y = atoi(av[3]);
	if (av[2][0] == '+')
		x += y;
	else if (av[2][0] == '-')
		x -= y;
	else if (av[2][0] == '*')
		x *= y;
	else if (y == 0)
		x = 0;
	else if (av[2][0] == '/' && x != 0)
		x /= y;
	else if (av[2][0] == '%' && x != 0)
		x %= y;
	printf("%d\n", x);
	return (0);
}
