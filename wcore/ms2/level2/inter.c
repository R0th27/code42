#include <unistd.h>

int	main(int ac, char **av)
{
	int	flag[128] = {0};
	int	i;

	if (ac == 3)
	{
		i = -1;
		while (av[2][++i])
			flag[(int)av[2][i]] = 1;
		i = -1;
		while (av[1][++i])
		{
			if (flag[(int)av[1][i]] == 1)
			{
				write(1, &av[1][i], 1);
				flag[(int)av[1][i]] = 0;
			}
		}
	}	
	write(1, "\n", 1);
	return (0);
}
