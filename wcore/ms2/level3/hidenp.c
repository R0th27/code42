#include <stdio.h>
#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	int		j;

	if (ac != 3)
		return (write(1, "\n", 1), 1);
	i = 0;
	j = -1;
	while (av[1][i] && av[2][++j])
	{
		if (av[2][j] == av[1][i])
			i++;
	}
	if (!av[1][i])
		write(1, "1", 1);
	else
		write(1, "0", 1);
	return (write(1, "\n", 1), 0);
}
