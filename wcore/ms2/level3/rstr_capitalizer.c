#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac != 2)
		return (write(1, "\n", 1), 1);
	i = -1;
	while (av[1][++i])
	{
		if (av[1][i] >= 'A' && av[1][i] <= 'Z')
			av[1][i] += 32;
		if (av[1][i] >= 'a' && av[1][i] <= 'z' && (av[1][i + 1] == ' ' || (av[1][i + 1] >= 9 && av[1][i + 1] <= 13) || !av[1][i + 1]))
			av[1][i] -= 32;
		write(1, &av[1][i], 1);
	}	
	return (write(1, "\n", 1), 0);
}
