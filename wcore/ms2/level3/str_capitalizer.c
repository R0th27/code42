#include <unistd.h>

int	main(int ac, char **av)
{
	int	flag;
	int	i;

	if (ac != 2)
		return (write(1, "\n", 1), 1);
	flag = 1;
	i = -1;
	while (av[1][++i])
	{
		if (av[1][i] >= 'A' && av[1][i] <= 'Z')
			av[1][i] += 32;
		if (av[1][i] >= 'a' && av[1][i] <= 'z' && flag == 1)
		{
			flag = 0;
			av[1][i] -= 32;
		}
		if (av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13))
			flag = 1;
		write(1, &av[1][i], 1);
	}
	return (write(1, "\n", 1), 0);
}
